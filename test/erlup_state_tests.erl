-module(erlup_state_tests).

-include_lib("eunit/include/eunit.hrl").

%%----------------------------------------------------------------------------------------------------------------------
%% Unit Tests
%%----------------------------------------------------------------------------------------------------------------------

sed_test_() ->
    [
     ?_assertEqual([1,2,3,4,[5]], erlup_state:sed([1,2,6,[4],[7]], [{6,3}, {[4], 4}, {7, 5}])),
     ?_assertEqual({1,2,3,4,{5}}, erlup_state:sed({1,2,6,{4},{7}}, [{6,3}, {{4}, 4}, {7, 5}]))
    ].
